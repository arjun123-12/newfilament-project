            panelNum.textContent = String(index + 1).padStart(2, '0') + ' / 12';
            panelName.textContent = srv.name;
            panelDesc.textContent = srv.desc;
            panelFeatures.innerHTML = srv.chips.map(c => `<span class="srv-panel-chip">${c}</span>`).join('');

            // activate new
            activeIndex = index;
            items[activeIndex].classList.add('is-active');
            panelImgs[activeIndex].classList.add('is-visible');
            dotsWrap.children[activeIndex].classList.add('is-active');

            // show content
            setTimeout(() => {
                panelContent.classList.add('is-visible');
                panelLine.classList.add('is-visible');
                isAnimating = false;
            }, 80);
        }, 300);
